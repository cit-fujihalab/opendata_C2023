CREATE TABLE postgres.t_events(
	id serial4 NOT NULL,
	"date" varchar(8) NOT NULL,
	"timestamp" timestamptz NOT NULL,
	company_id int4 NOT NULL,
	office_id int4 NOT NULL,
	car_number_id int4 NOT NULL,
	user_id int4 NOT NULL,
